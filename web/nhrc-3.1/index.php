<p>
The NHRC-3.1 continues the long standing tradition set by the NHRC-3. It has a
tremendous amount of functionality packed in a design with a very low
parts count, resulting in exceptional reliability at an affordable price.
<p>
The controller is programmable by sending DTMF sequences, or with NHRC's <i>free</i> Windows-based
programming software.  The CW ID, hang time, ID timer, timeout timer and tail message counter
can all be programmed by the user.  All programming is password-protected, and is
stored in non-volatile EEPROM memory.
