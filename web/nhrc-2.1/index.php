The NHRC-2.1 is the latest generation of  of NHRC's popular
<a href="/nhrc-2/">NHRC-2</a> Repeater
Controller.  The NHRC-2.1 offers many improvements over the NHRC-2, including
a fan control output, LEDs to display input and output status, and <i>many</i>
new programming features.
<p>
The NHRC-2.1 continues the long standing tradition set by the NHRC-2. It has a
tremendous amount of functionality packed in a design with a very low
parts count, resulting in exceptional reliability at an affordable price.
<p>
The controller is programmable by sending DTMF sequences.
The CW ID, hang time, ID timer, timeout timer and tail message counter
can all be programmed by the user.  All programming is password-protected, and
is stored in non-volatile EEPROM memory.
<p>
Just like the NHRC-2, the NHRC-2.1 is available as a partial kit you can build
in your own workshop.  All the parts are through-hole, and it can be assembled
with common electronics hand tools.  The partial kit includes the printed
circuit board and all four ICs.  Complete documentation is below.
