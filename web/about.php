the land-mobile radio industry, and have excellent relationships with 
many of the country's top radio shops.</p>
<p class="indent">
We have been building and designing repeater controllers since 1992, and
been operating NHRC LLC since 1998.  We have sold <i>thousands</i> of 
repeater controllers and related accessories to federal, state, and 
local governments, two-way radio shops and government contractors, 
amateur radio clubs, and individual amateur radio operators all over the 
world. There are NHRC products operating repeaters in all 50 American 
states and six of the seven continents!  There is probably a NHRC 
controller operating a repeater near you.</p>
