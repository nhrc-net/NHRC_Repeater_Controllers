<div class="pageHeadCentered">NHRC-10 Programming Software</div>
The NHRC-10 Repeater Controller Programming Software allows NHRC-10 owners
the ability to edit and save NHRC-10 repeater controller configuration data
on their own windows computer.<p>
The software communicates to the controller through the controller's CI-V 
interface connector.  A CI-V level converter is required to interface the
controller with the computers RS-232 port.  The CI-V converter is not
supplied by NHRC.<p>
The NHRC-10 Programming Software is supported by NHRC-10 firmware version
1.2 and newer. 
