<div class="productFeatures">Features:</div>
<ul>
  <li>True Speech
  <ul>
    <li>Four real speech ID messages you record yourself.
    <li>Three real speech tail messages you record yourself.
    <li>More than 90 real speech canned words used for controller messages and prompts.
    <li>Speech can be custom recorded for local language support.
    <li>Six-slot Voice Mailbox.
    <li>Audio Test Mode records user's audio and replays to user.
  </ul>
  <li>Autopatch
  <ul>
    <li>Full-duplex autopatch.
    <li>250 Autodial slots.
    <li>Support for up to 16 area codes with grant/deny access control to each exchange.
    <li>Ten emergency autodial slots.
    <li>&quot;Reverse Patch&quot; & DTMF control over telephone.<br>
		- Dedicated DTMF receiver for telephone control.
  </ul>
  <li>Link Port
  <ul>
    <li>Allows remote base, link radio, or slave repeater to be connected.
    <li>Unique courtesy tones indicate link/remote base status.
    <li>Link port can have DTMF control.
    <li>CI-V control of most Icom HF radios, including the IC-706 MkII G,
    provides a frequency-agile remote base.
  </ul>
  <li>Other Features
  <ul>
    <li>Separate control receiver audio input with dedicated DTMF receiver.<br>
    <li>DTMF receiver is available for link port if control receiver is not installed.
    <li>LiTZ & 911 message support.<br>
    <li>Will play dedicated voice message in response to LiTZ or 911 tones
        to tell user how to use emergency autodials, what other repeater to
        use, etc.
    <li>Transmitter Fan Control Output.
    <li>13 timers.
    <li>Over 70 control operator functions.
    <li>Five saved setup states.
    <li>Real sine wave audio tones.
    <li>DTMF access mode.
    <li>&quot;Carrier,&quot; &quot;Carrier and CTCSS,&quot; and &quot;Carrier or CTCSS&quot; access modes.
    <li>Granular security allows tailored control operator access.
    <li>DTMF pad test.
    <li>Jumper-selectable audio de-emphasis for main and link receivers.
  </ul>
</ul>
