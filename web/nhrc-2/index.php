<div class="productFeatures">Features:</div>
<ul>
  <li>4 stored voice messages (initial ID, normal ID, timeout, and tail or courtesy beep message)
  <li>&quot;Intelligent&quot; ID algorithm.
  <li>3 Operating Modes:
  <ul>
    <li>Duplex Repeater Controller
    <li>Simplex Repeater Controller
    <li>Link Controller
  </ul>
  <li>Touch-Tone remote control and programming.
  <li>Hang timer, ID timer, Timeout timer.
  <li>Extremely low power consumption.
  <li>Can be built for less than $60.00 (US).
</ul>
