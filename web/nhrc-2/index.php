The NHRC-2 is an inexpensive repeater controller with real stored speech
and DTMF remote control.  It is available assembled and tested, or as a 
partial kit.  The partial kit contains the printed circuit board, a 
<i>programmed</i> PIC microcontroller, a MT8870 DTMF
decoder, and a complete manual on CD-ROM.
The manual contains ordering information
for all the remaining parts needed to build the controller.  Typical cost
for building a NHRC-2 (including the partial kit itself) is around $60.<p> 
<p>
NHRC is pleased to present the NHRC-2 revision D Printed Circuit board!
This new layout features a smaller footprint, improved power routing to 
the ISD chip for less noise, as well as silkscreened parts placement on
the component side to simplify assembly.
