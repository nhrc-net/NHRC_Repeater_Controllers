<font size=6><b>Combination Numbers</b></font><br>
</center>
<p>
From the outside of the cabinet, the MASTR II mobile radios all look the same.
The &quot;combination number&quot; is the key to decode what band the radio
was built for, and what options were shipped inside or with the radio.  Use
these combination number tables to decipher the combination numbers and choose
the right radio for your conversion.  The frequency ranges that are most easily
converted to amateur band operation are marked in red.<p>
The information in these tables was gleaned from several MASTR II manuals.<p>

