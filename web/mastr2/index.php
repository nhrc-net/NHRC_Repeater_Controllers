<p>
The General Electric MASTR II mobile radio (and it's variants: the MASTR
Executive II and the Custom MVP) is very popular with amateur radio
operators for it's ease in converting to duplex operation for repeater
construction.  In addition to that, the radio is easy to service, and is widely
available for relatively inexpensive purchase at hamfests and from used
equipment dealers.<p>
These pages are intended to help amateurs make repeaters out of MASTR IIs.<p>
