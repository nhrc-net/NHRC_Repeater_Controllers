    <td>
The NHRC-3 repeater controller is an inexpensive controller with 90 seconds
of stored speech.  The controller can operate a normal full-duplex repeater,
or can operate a simplex repeater.  In normal repeater service, there are
four voice messages: an initial ID, a normal ID, a timeout message, and a
tail message or courtesy tone message.  In simplex repeater service, up to 
90 seconds of a user's transmission will be recorded and retransmitted by 
the repeater.<p>
The controller is programmable by sending DTMF sequences.  The CW ID, hang
time, ID timer, timeout timer and tail message counter can all be 
programmed by the user.  All programming is password-protected, and is
stored in non-volatile EEPROM memory.<p>
The controller has very low power consumption--less than 20 mA with the
LEDs disabled--making it ideal for use in a solar-powered repeater.
