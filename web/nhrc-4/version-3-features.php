<div class="pageHeadCentered">NHRC-4 Version 3.0 Software</div>

NHRC is pleased to announce the NHRC-4 version 3.0 software.
Version 3.0 is a completely new implementation of the NHRC-4 derived from our
latest code base.  We wanted to make the controller easier to use and as
powerful as we could without changing the hardware.
<p>
The NHRC-4 Version 3.0 Sofware is compatible with the entire NHRC-4 line,
including the <a href="/nhrc-4m2/index.php">NHRC-4/M2</a> and 
the <a href="/nhrc-4mvp/index.php">NHRC-4/MVP</a>.  It is a drop-in
replacement of the microprocessor chip.
<p>
We have released all new documentation for the NHRC-4 series repeater
controllers with software version 3.0.  All new NHRC-4 products will ship
with the new version 3.0 software.
<div class="productFeatures">Features:</div>
<blockquote>
  This list describes the new features in the version 3.0 software.
  Version 3.0 includes all previous NHRC-4 features.
</blockquote>
