      The NHRC-&mu; (&quot;micro&quot;) is a compact repeater controller/IDer
      designed for easy installation in close quarters.  The NHRC-&mu; can
      function as a conventional repeater controller, as an IDer for repeaters,
      or as a beacon controller.  It can be easily mounted with double-sided
      foam adhesive tape, and connected to the equipment with flying leads,
      allowing easy installation in almost any equipment.
      <p>
      The NHRC-&mu; is programmable by sending DTMF sequences over the air,
      or by computer with a serial port connection and the free NHRC-Micro
      programming software.  The CW ID, hang time, ID timer, timeout timer
      and courtesy tone beeps can all be programmed by the user.  All
      programming is stored in non-volatile EEPROM memory.
