<div class="productFeatures">Features:</div>
<ul>
  <li>Plugs in inside a General Electric MASTR II mobile with no complicated wiring.  Fits in Base Stations, too!
  <li>A single connector interfaces an optional CommSpec TS-64 for full CTCSS
      encode and decode.
  <li>4 user-recordable, non-volatile voice messages:
  <ul>
    <li>Initial ID Message
    <li>Normal ID Message
    <li>Timeout Message
    <li>Tail Message/Custom Courtesy Tone
  </ul>
  <li>&quot;Intelligent&quot; ID algorithm.
  <li>3 Operating Modes:
  <ul>
    <li>Duplex Repeater Controller
    <li>Simplex Repeater Controller
    <li>Link Controller
  </ul>
  <li>Touch-Tone remote control and programming.
  <li>Hang timer, ID timer, Timeout timer, and CW ID stored in non-volatile memory.
  <li>Audio gating allows use of non-squelched receiver audio.
  <li>LED CAS, PTT, and DTMF indicators.
  <li>Supports optional NHRC-DAD digital audio delay for squelch tail
      elimination/reduction and total muting of DTMF.
  <li>Extremely low power consumption.
</ul>
