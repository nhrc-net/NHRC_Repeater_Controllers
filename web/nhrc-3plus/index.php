The NHRC-3+ is a new, enhanced version of NHRC's popular
<a href="/nhrc-3/">NHRC-3</a> Repeater
Controller.  The NHRC-3+ offers many improvements over the NHRC-3, including
simplified programming, a CTCSS input, multi-tone courtesy tones, a fan
control output, four digital outputs and two courtesy tone select inputs.
<p>
The NHRC-3+ continues the tradition set by the NHRC-2 and NHRC-3. It has a
tremendous amount of functionality packed in a design with a very low
parts count, resulting in exceptional reliability at an affordable price.
<p>
The controller is programmable by sending DTMF sequences.  The CW ID, hang
time, ID timer, timeout timer and tail message counter can all be
programmed by the user.  All programming is password-protected, and is
stored in non-volatile EEPROM memory.
