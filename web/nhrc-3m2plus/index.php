    <li>Dedicated Fan Control/Digital output.
    <li>6 Programming Commands.
    <li>29 Control Operator Settings.
    <li>3 User Commands.
    <li>Two saved setups.
    <li>Touch-Tone remote control and programming.
    <li>Hang timer, ID timer, Timeout timer, Fan Timer, and CW messages stored in non-volatile memory.
    <li>Two Digital Inputs
    <ul>
      <li>Courtesy Tone Select 1.
      <li>Courtesy Tone Select 2.
    </ul>
    <li>Four Digital Outputs.
    <li>Jumper-selectable receiver deemphasis.
